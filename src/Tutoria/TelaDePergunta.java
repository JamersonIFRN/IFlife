    public boolean verSeJaLiberou(String jogo) {
        String arquivo = "Res/Dados//JogosDesbloqueados.txt", lin, f = "";
        try {
            FileReader fr = new FileReader(arquivo);
            BufferedReader br = new BufferedReader(fr);
            try {
                while (br.ready()) {
                    lin = "";
                    lin += br.readLine();
                    if (!lin.equals("")) {
                        f += lin + "\n";
                    }
                }
                if (!f.equals("")) {
                    f = f.substring(0, f.length() - 1);
                }
            } catch (IOException ex) {
                Logger.getLogger(TelaDePergunta.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(TelaDePergunta.class.getName()).log(Level.SEVERE, null, ex);
        }
        String jogos[] = f.split("-");
        int j = 0;
        for (int i = 0; i < jogos.length; i++) {
            if (jogos[i].equals(jogo)) {
                j++;
            }
        }
        if (j == 0) {
            return true;
        }
        return false;
    }

    public void acertou() {
        JOptionPane.showMessageDialog(null, "Parabéns, acertou a pergunta.\nLiberou um novo jogo");
        String arquivo = "Res/Dados/JogosDesbloqueados.txt";
        String jogo = "";
        switch (ano) {
            case "2":
                if (verSeJaLiberou("caca")) {
                    jogo = "-caca";
                }
                break;
            case "3":
                if (verSeJaLiberou("velha")) {
                    jogo = "-velha";
                }
                break;
            case "4":
                if (verSeJaLiberou("cruzadas")) {
                    jogo = "-cruzadas";
                }
                break;
        }
        FileWriter fw;
        try {
            fw = new FileWriter(arquivo, true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(jogo);
            bw.close();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(TelaDePergunta.class.getName()).log(Level.SEVERE, null, ex);
        }
        cont = true;
    }

    public void errou() {
        JOptionPane.showMessageDialog(null, "Errou a pergunta!\nA Resposta certa era: " + respostacerta);
        cont = true;
    }
