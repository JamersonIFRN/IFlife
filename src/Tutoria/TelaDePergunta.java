    private void setarPergunta(String materia, String ano) {
        String arquivo = "Res/Tutoria/" + materia + "/" + ano + "/perguntas.txt", linha = "", f = "";
        try {
            FileReader fr = new FileReader(arquivo);
            BufferedReader br = new BufferedReader(fr);
            try {
                while (br.ready()) {
                    linha = "";
                    linha += br.readLine();
                    if (!linha.equals("")) {
                        f += linha + "\n";
                    }
                }
                f = f.substring(0, f.length() - 1);
            } catch (IOException ex) {
                Logger.getLogger(Fase.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Fase.class.getName()).log(Level.SEVERE, null, ex);
        }
        String perguntas[] = f.split("\n");
        Random r = new Random();
        int numerodapergunta = r.nextInt(perguntas.length);
        String perguntasAlternativasEResposta[] = perguntas[numerodapergunta].split("@");
        String texto[] = perguntasAlternativasEResposta[0].split("&");
        jLabel3.setText("<html>");
        for (int i = 0; i < texto.length; i++) {
            if (i != texto.length - 1) {
                jLabel3.setText(jLabel3.getText() + "<br><p>" + texto[i] + "</p>");
            } else {
                jLabel3.setText(jLabel3.getText() + "<br><p>" + texto[i] + "</p></html>");
            }
        }
        String alternativas[] = perguntasAlternativasEResposta[1].split("-");
        jButton2.setText(alternativas[0]);
        jButton6.setText(alternativas[1]);
        jButton7.setText(alternativas[2]);
        jButton9.setText(alternativas[3]);
        resposta = perguntasAlternativasEResposta[2];
        respostacerta = alternativas[Integer.parseInt(resposta) - 1];
    }
